<p>
Other philosophers have held that knowledge of reality is not derived from a priori principles, but is obtained only from experience. This type of metaphysics is called empiricism. Still another school of philosophy has maintained that, although an ultimate reality does exist, it is altogether inaccessible to human knowledge, which is necessarily subjective because it is confined to states of mind. Knowledge is therefore not a representation of external reality, but merely a reflection of human perceptions. This view is known as skepticism or agnosticism in respect to the soul and the reality of God.
</p>

<p>Arguments against belief in God are as numerous as arguments for it. Atheists absolutely deny the existence of God. Some, for instance, believe the material universe constitutes ultimate reality; others argue that the prevalence of suffering and evil in the world precludes the existence of a sacred being. Agnostics believe that the evidence for and against the existence of God is inconclusive; they therefore suspend judgment (see Agnosticism). Positivists believe that rational inquiry is restricted to questions of empirical fact, so that it is meaningless either to affirm or deny the existence of God (see Positivism).
</p>

