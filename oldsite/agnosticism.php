<p>
Concerning the gods I have no means of knowing either that they exist or that they do not exist nor what sort of form they have.<br>
Protagoras (480? BC - 411? BC)
</p>
<p>
Science has proof without any certainty. Creationists have certainty without any proof.<br>
Attributed to Ashley Montagu (1905 - 1999)
