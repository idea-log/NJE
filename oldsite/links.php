Education has taken a whole different proportion in the 21st century. Schools and universities have become obsolete just as TV has become outdated upon the arrival of Internet. Of course there are still a lot of people tenaciously  watching T.V. just as there are a lot of institutions perniciously to the ways of the past. Unfortunately, social change doesn't come rapidly. This is the major problem of our age: technology develops at exponential rates while social change is unable to keep up with it. We catch up late, and with the scale and impact of the technologies that we have, it might be too late too soon. We either find a solution for our problems or life on earth may come to an end.
