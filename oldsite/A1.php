SVOPT

</p>

<h2>Subject Pronouns</h2>
<p>
I 			(am) 		example: I am single				<br>		
you 		(are)		example: You are nice				<br>	
he/she/it	(is)		example: He is handsome				<br>	
we 			(are)		example: She is beautiful			<br>	
you 		(are)		example: You are good students		<br>	
They 		(are)		example: They are not coming today
</p>

